=begin
    El método send puede ser usado para llamar métodos
    públicos y privados de un objeto.
    El método public_send solo llama métodos públicos.

    Send existe desde la versión 1 de Ruby, public_send
    se creó principalmente para aquellos programadores
    que deseaban dejar bién vista la intención de llamar
    a métodos públicos y no joder con los atributos de los objetos
=end